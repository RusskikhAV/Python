"""
        Большое число

Дети никак не успокоятся и продолжают «мучить» числа. Сейчас они хотят общими силами составить очень большое число.
Каждый ребёнок называет число состоящее из цифр, которые он знает.
Напишите программу, которая строит число, состоящее из максимальных цифр каждого ребёнка.

Формат ввода:
    В первой строке указано число N — количество детей в группе.
    В каждой из последующих N строк записано число.

Формат вывода:
    Одно большое число.
"""

# считывание и сохранение введенных с клавиатуры данных
quantity_of_child = int(input())
res_number = ""

# решим без вложенного цикла, с помощью метода max()
for i in range(quantity_of_child):
    curr_num = input()
    max_num = max(curr_num)
    res_number += max_num
print(res_number)
